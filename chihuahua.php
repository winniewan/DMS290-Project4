            <h3>Care</h3>
            
            <h4>Health</h4>
            <p>
                The majority of Chihuahuas are healthy little dogs, but there are some genetic issues that can affect the breed. 
                You should choose your dog from a breeder who routinely does genetic health screenings on all breeding stock to 
                ensure that the puppies they produce are as genetically sound as possible. Some of the issues that can possibly 
                affect the Chihuahua include potential heart problems (patent ductus arteriosus, mitral valve disease), eye 
                disease, and patellar luxation (loose kneecaps). Idiopathic epilepsy is also known to occur in the breed.
            </p>
            
            <h4>Diet</h4>
            <p>
                A high-quality dog food appropriate to the dog’s age (puppy, adult, or senior) will have all the 
                nutrients the breed needs. Some Chihuahuas are prone to getting overweight, so watch your dog’s 
                calorie consumption and weight level. Treats can be an important aid in training, but giving too 
                many can cause obesity. Give table scraps sparingly, if at all, especially avoiding cooked bones 
                and foods with high fat content. Learn about which human foods are safe for dogs, and which are not. 
                Check with your vet if you have any concerns about your dog’s weight or diet.
            </p>

            <h4>Grooming</h4>
            <p>
                The two coat varieties of the Chihuahua have slightly different grooming needs. The smooth-coat Chihuahua 
                will need only occasional brushing and regular baths to look dapper, while the longhaired variety should 
                have his coat brushed at least once a week to avoid any tangles or mats. Both varieties should have their 
                nails trimmed regularly. Good dental care is necessary and should include brushing your dog’s teeth, and 
                the vet might also recommend treats designed as part of a tooth-care program. Check the Chihuahua’s ears 
                regularly, and remove any excess wax or debris to avoid ear infections.
