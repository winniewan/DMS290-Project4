            <h4>Activities</h4>
            <p>
                The Chihuahua loves to run and play and can usually get enough exercise in a very small space. 
                Simply trotting around following their people is usually enough exercise for this happy breed. 
                Short, slow walks will keep your dog in good weight and condition. Avoid overexerting the Chihuahua. 
                If your dog is panting and working hard to keep up, it’s time to pick him up and carry him home.
            </p>

            <h4>Training</h4>
            <p>
                The Chihuahua is a very alert little dog of high intelligence. He is eager to please his humans and 
                responds well to positive training practices. Chihuahuas seem well aware of how cute they are and learn 
                how to get their way. From the very beginning you must enforce the fact that you are in charge. Never 
                allow your Chihuahua puppy to do anything that will be unacceptable in an adult. They can have a bit 
                of a “terrier” temperament, so a firm but gentle hand is necessary when training. They can excel in 
                obedience training and other canine sports.
