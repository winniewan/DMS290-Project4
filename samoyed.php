            of Argenteau made history in 1906 as the first of the breed (then known as the Samoyede) registered by the AKC. American and
            European mushers of the early 20th century knew the Samoyed as an enthusiastic hauler of supplies, capable of sledging one and
            a half times their own weight. Scott, Shackleton, and Amundsen are a few of the era’s famous explorers who rode behind Sammies
